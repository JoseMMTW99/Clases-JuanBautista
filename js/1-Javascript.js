// Introducción a JavaScript

/*
JavaScript es un lenguaje de programación usado para hacer las páginas web interactivas.
Con JavaScript, podemos hacer que cosas pasen cuando haces clic en botones, escribes en formularios, o mueves el mouse.

Vamos a aprender los conceptos básicos de JavaScript paso a paso.
*/

// 1. Variables
/*
Una variable es como una caja donde puedes guardar un valor (un número, un texto, etc.) para usarlo más tarde.

Hay tres formas principales de declarar una variable en JavaScript:
  - var (no lo usaremos mucho porque es antiguo)
  - let (se usa cuando la variable puede cambiar)
  - const (se usa cuando el valor no va a cambiar)
*/

// Ejemplos:
let nombre = "Juan"; // Aquí estamos guardando el nombre "Juan" en la variable 'nombre'
const edad = 14; // Aquí estamos guardando la edad 14 en una variable que no va a cambiar

/*
Puedes cambiar el valor de una variable declarada con let, pero no con const.
*/

// 2. Tipos de Datos
/*
En JavaScript, hay diferentes tipos de datos que puedes guardar en las variables. Los más comunes son:

- Números: para representar cantidades.
- Cadenas de texto (Strings): para palabras o frases.
- Booleanos: para verdadero (true) o falso (false).
*/

// Ejemplos:
let numero = 42; // Número
let texto = "Hola, soy un texto"; // Cadena de texto
let esVerdad = true; // Booleano (true o false)

